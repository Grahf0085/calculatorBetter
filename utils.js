export function add(someNumber, otherNumber) {
    return someNumber + otherNumber;
}

export function sub(someNumber, otherNumber) {
    return someNumber - otherNumber;
}

export function mul(someNumber, otherNumber) {
    return someNumber * otherNumber;
}

export function div(someNumber, otherNumber) {
    return someNumber / otherNumber;
}